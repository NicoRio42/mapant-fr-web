	<h1>A propos de Mapant.fr</h1>

	<p>
		Le projet Mapant.fr a pour but de créer la <strong text-primary
			>carte de France la plus détaillée de tous les temps</strong
		>
		! Il est inspiré de ses prédécesseurs
		<a href="https://www.mapant.fi/" target="_blank">finlandais</a>,
		<a href="https://mapant.no" target="_blank">norvégien</a>,
		<a href="https://kartor.gokartor.se/" target="_blank">suédois</a>,
		<a href="https://mapant.es" target="_blank">espagnols</a>,
		<a href="https://www.mapant.ch/" target="_blank">suisse</a> et
		<a href="https://mapant.orienteering.lu/" target="_blank">luxembourgeois</a>. Nous générons la
		carte à partir de
		<a href="https://fr.wikipedia.org/wiki/Lidar" target="_blank">données LiDAR</a>, disponibles
		librement grâce au projet
		<a href="https://geoservices.ign.fr/lidarhd" target="_blank">LiDAR HD</a> de l'<a
			href="https://www.ign.fr/"
			raget="_blank">Institut national de l'information géographique et forestière</a
		> (anciennement IGN). Nous utilisons aussi les données du projet Open Street Map.
	</p>

